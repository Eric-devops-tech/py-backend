def exportar_json(nome_arquivo, dados):
    try:
        with open(nome_arquivo, "w", encoding="utf-8") as arquivo:
            json.dump(dados, arquivo, ensure_ascii=False, indent=4, default=str)
        print(f"Dados exportados para {nome_arquivo}.\n")
    except IOError as erro:
        print(f"Erro ao exportar JSON: {erro}\n")


def consulta_pacientes_por_urgencia():
    print("\n--- Consulta filtrada: Pacientes por urgencia ---")
    urgencia_minima = obter_inteiro("Urgencia minima: ", minimo=0)
    pacientes = executar_sql(
        """
        SELECT id, nome, idade, tipo_dor, tempo_dor, renda, bairro, cidade, uf, urgencia
        FROM pacientes
        WHERE urgencia >= :urgencia
        ORDER BY urgencia DESC, nome
        """,
        {"urgencia": urgencia_minima},
        fetch=True,
    )
    if not pacientes:
        print("Nenhum paciente encontrado para esse filtro.\n")
        return

    for p in pacientes:
        print(f"[{p['id']}] {p['nome']} | Urgencia {p['urgencia']} | {p['bairro']} - {p['cidade']}/{p['uf']}")

    if input("Exportar resultado para JSON? (s/n): ").strip().lower() == "s":
        exportar_json(ARQUIVO_EXPORTACAO_PACIENTES, pacientes)
    print()


def consulta_dentistas_por_filtro():
    print("\n--- Consulta filtrada: Dentistas por bairro/especialidade ---")
    print("1. Filtrar por bairro")
    print("2. Filtrar por especialidade")
    opcao = obter_inteiro("Escolha: ", minimo=1, maximo=2)

    if opcao == 1:
        bairro = obter_texto_obrigatorio("Bairro: ").lower()
        sql = """
            SELECT id, nome, especialidade, bairro, cidade, uf, max_pacientes, atendidos
            FROM dentistas
            WHERE LOWER(bairro) LIKE :bairro
            ORDER BY nome
        """
        params = {"bairro": f"%{bairro}%"}
    else:
        especialidade = obter_opcao_lista("Especialidade: ", ESPECIALIDADES_VALIDAS)
        sql = """
            SELECT id, nome, especialidade, bairro, cidade, uf, max_pacientes, atendidos
            FROM dentistas
            WHERE especialidade = :especialidade
            ORDER BY nome
        """
        params = {"especialidade": especialidade}

    dentistas = executar_sql(sql, params, fetch=True)
    if not dentistas:
        print("Nenhum dentista encontrado para esse filtro.\n")
        return

    for d in dentistas:
        vagas = d["max_pacientes"] - d["atendidos"]
        print(f"[{d['id']}] {d['nome']} | {d['especialidade']} | {d['bairro']} - {d['cidade']}/{d['uf']} | Vagas {vagas}")

    if input("Exportar resultado para JSON? (s/n): ").strip().lower() == "s":
        exportar_json(ARQUIVO_EXPORTACAO_DENTISTAS, dentistas)
    print()


