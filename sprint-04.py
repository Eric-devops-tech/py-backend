def agendar_consulta():
    print("\n--- Agendar Consulta ---")
    listar_pacientes()
    paciente_id = obter_inteiro("ID do paciente: ", minimo=1)
    paciente = buscar_paciente_por_id(paciente_id)
    if not paciente:
        print("Paciente nao encontrado.\n")
        return

    data = obter_data()
    hora = obter_hora()

    usar_match = input("Usar match automatico de dentista? (s/n): ").strip().lower()
    dentista_id = None

    if usar_match == "s":
        dentista, pontuacao = encontrar_melhor_dentista(paciente)
        if dentista and pontuacao > 0:
            dentista_id = dentista["id"]
            print(f"Match realizado: {dentista['nome']} (pontuacao {pontuacao}).")
        else:
            print("Nenhum dentista compativel. Consulta ficara aguardando dentista.")
    else:
        listar_dentistas()
        escolhido = obter_inteiro("ID do dentista (0 para deixar sem dentista): ", minimo=0)
        if escolhido != 0 and buscar_dentista_por_id(escolhido):
            dentista_id = escolhido

    executar_sql(
        """
        INSERT INTO consultas (paciente_id, dentista_id, data_consulta, hora_consulta, status)
        VALUES (:paciente_id, :dentista_id, :data_consulta, :hora_consulta, 'AGENDADA')
        """,
        {
            "paciente_id": paciente_id,
            "dentista_id": dentista_id,
            "data_consulta": data,
            "hora_consulta": hora,
        },
        commit=True,
    )

    if dentista_id:
        executar_sql(
            "UPDATE dentistas SET atendidos = atendidos + 1 WHERE id = :id",
            {"id": dentista_id},
            commit=True,
        )

    print("Consulta agendada.\n")


def listar_consultas():
    print("\n--- Consultas Agendadas ---")
    consultas = executar_sql(
        """
        SELECT c.id, p.nome AS paciente, d.nome AS dentista,
               c.data_consulta, c.hora_consulta, c.status
        FROM consultas c
        JOIN pacientes p ON p.id = c.paciente_id
        LEFT JOIN dentistas d ON d.id = c.dentista_id
        ORDER BY c.id
        """,
        fetch=True,
    )
    if not consultas:
        print("Nenhuma consulta agendada.\n")
        return []

    for c in consultas:
        dentista = c["dentista"] or "Aguardando dentista"
        print(f"[{c['id']}] Paciente: {c['paciente']} | Dentista: {dentista}")
        print(f"    Data/Hora: {c['data_consulta']} as {c['hora_consulta']} | Status: {c['status']}")
        print("-" * 45)
    print(f"Total: {len(consultas)} consulta(s).\n")
    return consultas


def cancelar_consulta():
    print("\n--- Cancelar Consulta ---")
    listar_consultas()
    consulta_id = obter_inteiro("ID da consulta a cancelar (0 para voltar): ", minimo=0)
    if consulta_id == 0:
        return

    consulta = executar_sql(
        "SELECT dentista_id FROM consultas WHERE id = :id",
        {"id": consulta_id},
        fetch=True,
    )
    if not consulta:
        print("Consulta nao encontrada.\n")
        return

    dentista_id = consulta[0].get("dentista_id")
    executar_sql("DELETE FROM consultas WHERE id = :id", {"id": consulta_id}, commit=True)
    if dentista_id:
        executar_sql(
            "UPDATE dentistas SET atendidos = CASE WHEN atendidos > 0 THEN atendidos - 1 ELSE 0 END WHERE id = :id",
            {"id": dentista_id},
            commit=True,
        )
    print("Consulta cancelada.\n")


