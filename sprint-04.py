# VALIDACOES

def obter_inteiro(mensagem, minimo=None, maximo=None):
    while True:
        try:
            valor = int(input(mensagem))
            if minimo is not None and valor < minimo:
                print(f"Digite um valor maior ou igual a {minimo}.")
                continue
            if maximo is not None and valor > maximo:
                print(f"Digite um valor menor ou igual a {maximo}.")
                continue
            return valor
        except ValueError:
            print("Entrada invalida. Digite um numero inteiro.")


def obter_float(mensagem, minimo=None):
    while True:
        try:
            valor = float(input(mensagem).replace(",", "."))
            if minimo is not None and valor < minimo:
                print(f"Digite um valor maior ou igual a {minimo}.")
                continue
            return valor
        except ValueError:
            print("Entrada invalida. Digite um numero decimal.")


def obter_texto_obrigatorio(mensagem):
    while True:
        texto = input(mensagem).strip()
        if texto:
            return texto
        print("Campo obrigatorio. Tente novamente.")


def obter_opcao_lista(mensagem, opcoes):
    while True:
        valor = input(mensagem).strip().lower()
        if valor in opcoes:
            return valor
        print(f"Opcao invalida. Escolha entre: {', '.join(opcoes)}")


def validar_data(data):
    try:
        datetime.strptime(data, "%d/%m/%Y")
        return True
    except ValueError:
        return False


def validar_hora(hora):
    return re.fullmatch(r"([01]\d|2[0-3]):[0-5]\d", hora) is not None


def obter_data():
    while True:
        data = input("Data da consulta (DD/MM/AAAA): ").strip()
        if validar_data(data):
            return data
        print("Data invalida.")


def obter_hora():
    while True:
        hora = input("Hora da consulta (HH:MM): ").strip()
        if validar_hora(hora):
            return hora
        print("Hora invalida.")


# API PUBLICA: VIACEP

def buscar_cep(cep):
    """Consulta o ViaCEP e retorna os dados de endereco."""
    if requests is None:
        print("Biblioteca 'requests' nao instalada. Use: pip install requests")
        return None

    cep_limpo = re.sub(r"\D", "", cep)
    if len(cep_limpo) != 8:
        print("CEP invalido. Informe 8 numeros.")
        return None

    url = f"https://viacep.com.br/ws/{cep_limpo}/json/"
    try:
        resposta = requests.get(url, timeout=5)
        resposta.raise_for_status()
        dados = resposta.json()

        if dados.get("erro"):
            print("CEP nao encontrado.")
            return None

        return {
            "cep": dados.get("cep", ""),
            "logradouro": dados.get("logradouro", ""),
            "bairro": dados.get("bairro", ""),
            "cidade": dados.get("localidade", ""),
            "uf": dados.get("uf", ""),
        }
    except requests.RequestException as erro:
        print(f"Erro ao consultar API ViaCEP: {erro}")
        return None
    except ValueError:
        print("Resposta invalida da API ViaCEP.")
        return None


def obter_endereco_por_cep():
    """Pede CEP e completa endereco pela API publica ViaCEP."""
    cep = input("CEP (somente numeros ou com hifen): ").strip()
    endereco = buscar_cep(cep)

    if endereco:
        print(
            f"Endereco encontrado: {endereco['logradouro']}, "
            f"{endereco['bairro']} - {endereco['cidade']}/{endereco['uf']}"
        )
        return endereco

    print("Preencha o endereco manualmente.")
    return {
        "cep": cep,
        "logradouro": input("Logradouro: ").strip(),
        "bairro": obter_texto_obrigatorio("Bairro/Cidade: "),
        "cidade": input("Cidade: ").strip(),
        "uf": input("UF: ").strip().upper()[:2],
    }


# REGRAS DO PROJETO

def calcular_urgencia(tipo_dor="leve", tempo_dor=1, renda=1.0, idade=0):
    urgencia = 0

    if tipo_dor == "forte":
        urgencia += 3
    elif tipo_dor == "dente quebrado":
        urgencia += 4
    else:
        urgencia += 1

    if tempo_dor > 7:
        urgencia += 2
    if renda <= 2:
        urgencia += 1
    if 6 <= idade <= 17:
        urgencia += 2
    elif 18 <= idade <= 29:
        urgencia += 1

    return urgencia


def compatibilidade_especialidade(tipo_dor):
    mapa = {
        "leve": ["clinico geral", "odontopediatria"],
        "forte": ["endodontia", "clinico geral"],
        "dente quebrado": ["protese", "cirurgia"],
    }
    return mapa.get(tipo_dor, [])


def encontrar_melhor_dentista(paciente):
    dentistas = executar_sql(
        """
        SELECT id, nome, especialidade, bairro, cidade, uf, max_pacientes, atendidos
        FROM dentistas
        WHERE atendidos < max_pacientes
        """
        ,
        fetch=True,
    )

    melhor = None
    maior_pontuacao = -1
    especialidades = compatibilidade_especialidade(paciente["tipo_dor"])

    for dentista in dentistas:
        pontos = 0
        if dentista["especialidade"] in especialidades:
            pontos += 2
        if dentista.get("bairro") and paciente.get("bairro"):
            if dentista["bairro"].lower() == paciente["bairro"].lower():
                pontos += 3
        if dentista.get("cidade") and paciente.get("cidade"):
            if dentista["cidade"].lower() == paciente["cidade"].lower():
                pontos += 1
        if paciente["urgencia"] >= 5:
            pontos += 5

        if pontos > maior_pontuacao:
            maior_pontuacao = pontos
            melhor = dentista
        elif pontos == maior_pontuacao and melhor:
            if dentista["atendidos"] < melhor["atendidos"]:
                melhor = dentista

    return melhor, maior_pontuacao


